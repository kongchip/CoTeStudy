function solution(sizes) {
    sizes = sizes.map(([width, height]) => width > height ? [width, height] : [height, width])
    const widths = sizes.map(([width, height]) => width)
    const heights = sizes.map(([width, height]) => height)
    const maxWidth = Math.max(...widths)
    const maxHeight = Math.max(...heights)

    return maxWidth * maxHeight
}

